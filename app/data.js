      Math.abs(aggregate.T - profile.T) * 1.1 +
      Math.abs(aggregate.P - profile.P) * 1 +
      Math.abs(aggregate.U - profile.U) * 1.2;
    scores[id] += Math.max(0, 240 - distance);
