function detectLanguage() {
  const saved = localStorage.getItem("duzzonLang");
  if (saved && I18N[saved]) return saved;
  const lang = (navigator.language || "ko").toLowerCase();
  if (lang.startsWith("ar")) return "ar";
  if (lang.startsWith("en")) return "en";
  if (lang.startsWith("ja")) return "ja";
  if (lang.startsWith("zh")) return "zh";
  return "ko";
}

function setLanguage(lang) {
  if (!I18N[lang]) return;
  localStorage.setItem("duzzonLang", lang);
  applyI18n(lang);
  document.dispatchEvent(new CustomEvent("duzzon:lang", { detail: { lang } }));
}

function applyI18n(lang) {
  const dict = I18N[lang] || I18N.ko;
  document.documentElement.lang = lang;
  document.documentElement.dir = "ltr";
  if (document.body) {
    document.body.classList.toggle("lang-rtl", lang === "ar");
  }
  window.__duzzonLang = lang;

  document.querySelectorAll("[data-i18n]").forEach((el) => {
    const key = el.getAttribute("data-i18n");
    if (dict[key]) {
      el.textContent = dict[key];
    }
  });

  document.querySelectorAll("[data-i18n-html]").forEach((el) => {
    const key = el.getAttribute("data-i18n-html");
    if (dict[key]) {
      el.innerHTML = dict[key].replace(/\n/g, "<br />");
    }
  });

  const flagEmojiMap = { ko: "ðŸ‡°ðŸ‡·", en: "ðŸ‡ºðŸ‡¸", ja: "ðŸ‡¯ðŸ‡µ", zh: "ðŸ‡¨ðŸ‡³", ar: "ðŸ‡¦ðŸ‡ª" };
  const flagImageMap = {
    ko: "https://www.figma.com/api/mcp/asset/1d0435f7-e710-4879-af9a-9fcea6af5771",
    ar: "https://www.figma.com/api/mcp/asset/3f435256-68da-48f5-a882-f3e0158de018",
    en: "https://www.figma.com/api/mcp/asset/fe8b3fea-fc05-4fbe-bc70-0f74185d2a24",
    ja: "https://www.figma.com/api/mcp/asset/052c61aa-085a-443d-8ca4-be9dd0d9c566",
    zh: "https://www.figma.com/api/mcp/asset/3da58ab4-736d-4a28-a60b-b41c37457edf"
  };
  document.querySelectorAll(".flag").forEach((el) => {
    if (el.tagName === "IMG") {
      el.style.display = "";
      const oldFallback = el.parentElement.querySelector(".flag-fallback");
      if (oldFallback) oldFallback.remove();
      el.src = flagImageMap[lang] || flagImageMap.ko;
      el.alt = flagEmojiMap[lang] || "ðŸŒ";
      el.onload = () => {
        el.style.display = "";
        const fallback = el.parentElement.querySelector(".flag-fallback");
        if (fallback) fallback.remove();
      };
      el.onerror = () => {
        el.style.display = "none";
        const fallback = document.createElement("span");
        fallback.className = "flag-fallback";
        fallback.textContent = flagEmojiMap[lang] || "ðŸŒ";
        fallback.style.fontSize = "24px";
        fallback.style.lineHeight = "1";
        if (!el.parentElement.querySelector(".flag-fallback")) {
          el.parentElement.appendChild(fallback);
        }
      };
    } else {
      el.textContent = flagEmojiMap[lang] || "ðŸŒ";
    }
  });
}

